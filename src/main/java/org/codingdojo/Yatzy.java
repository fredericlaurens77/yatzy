package org.codingdojo;

import java.util.Set;

import static org.codingdojo.Face.*;

public class Yatzy {

    public static int scoreChance(Roll roll)
    {
        return roll.sum();
    }

    public static int scoreYatzy(Roll roll) {
        if (roll.facesOccurringAtLeast(5).isEmpty()) {
            return 0;
        }
        return 50;
    }

    public static int ScoreOnes(Roll roll) {
        return roll.sumOfAll(ONE);
    }

    public static int ScoreTwos(Roll roll) {
        return roll.sumOfAll(TWO);
    }

    public static int ScoreThrees(Roll roll) {
        return roll.sumOfAll(THREE);
    }

    public static int ScoreFours(Roll roll)
    {
        return roll.sumOfAll(FOUR);
    }

    public static int ScoreFives(Roll roll)
    {
        return roll.sumOfAll(FIVE);
    }

    public static int ScoreSixes(Roll roll)
    {
        return roll.sumOfAll(SIX);
    }

    public static int ScorePair(Roll roll)
    {
        Set<Face> facesFound = roll.facesOccurringAtLeast(2);
        if(facesFound.isEmpty()){
            return 0;
        }

        int highestPair = facesFound.stream().mapToInt(Face::intValue).max().getAsInt();
        return highestPair*2;
    }

    public static int ScoreTwoPairs(int d1, int d2, int d3, int d4, int d5)
    {
        int[] counts = new int[6];
        counts[d1-1]++;
        counts[d2-1]++;
        counts[d3-1]++;
        counts[d4-1]++;
        counts[d5-1]++;
        int n = 0;
        int score = 0;
        for (int i = 0; i < 6; i += 1)
            if (counts[6-i-1] >= 2) {
                n++;
                score += (6-i);
            }
        if (n == 2)
            return score * 2;
        else
            return 0;
    }

    public static int ScoreFourOfAKind(int d1, int d2, int d3, int d4, int d5)
    {
        int[] tallies;
        tallies = new int[6];
        tallies[d1-1]++;
        tallies[d2-1]++;
        tallies[d3-1]++;
        tallies[d4-1]++;
        tallies[d5-1]++;
        for (int i = 0; i < 6; i++)
            if (tallies[i] >= 4)
                return (i+1) * 4;
        return 0;
    }

    public static int ScoreThreeOfAKind(int d1, int d2, int d3, int d4, int d5)
    {
        int[] t;
        t = new int[6];
        t[d1-1]++;
        t[d2-1]++;
        t[d3-1]++;
        t[d4-1]++;
        t[d5-1]++;
        for (int i = 0; i < 6; i++)
            if (t[i] >= 3)
                return (i+1) * 3;
        return 0;
    }

    public static int scoreSmallStraight(int d1, int d2, int d3, int d4, int d5)
    {
        int[] tallies;
        tallies = new int[6];
        tallies[d1-1] += 1;
        tallies[d2-1] += 1;
        tallies[d3-1] += 1;
        tallies[d4-1] += 1;
        tallies[d5-1] += 1;
        if (tallies[0] == 1 &&
                tallies[1] == 1 &&
                tallies[2] == 1 &&
                tallies[3] == 1 &&
                tallies[4] == 1)
            return 15;
        return 0;
    }

    public static int scoreLargeStraight(int d1, int d2, int d3, int d4, int d5)
    {
        int[] tallies;
        tallies = new int[6];
        tallies[d1-1] += 1;
        tallies[d2-1] += 1;
        tallies[d3-1] += 1;
        tallies[d4-1] += 1;
        tallies[d5-1] += 1;
        if (tallies[1] == 1 &&
                tallies[2] == 1 &&
                tallies[3] == 1 &&
                tallies[4] == 1
                && tallies[5] == 1)
            return 20;
        return 0;
    }

    public static int scoreFullHouse(int d1, int d2, int d3, int d4, int d5)
    {
        int[] tallies;
        boolean _2 = false;
        int i;
        int _2_at = 0;
        boolean _3 = false;
        int _3_at = 0;




        tallies = new int[6];
        tallies[d1-1] += 1;
        tallies[d2-1] += 1;
        tallies[d3-1] += 1;
        tallies[d4-1] += 1;
        tallies[d5-1] += 1;

        for (i = 0; i != 6; i += 1)
            if (tallies[i] == 2) {
                _2 = true;
                _2_at = i+1;
            }

        for (i = 0; i != 6; i += 1)
            if (tallies[i] == 3) {
                _3 = true;
                _3_at = i+1;
            }

        if (_2 && _3)
            return _2_at * 2 + _3_at * 3;
        else
            return 0;
    }

}
